BOOK_STATUS_ORDER = ("want_to_read", "reading", "finished", "paused")
ANNOTATION_TAG_ORDER = ("favorite", "question", "idea", "character", "foreshadowing")


def normalize_language(raw_language: str | None) -> str:
    value = (raw_language or "").strip().lower()
    return value if value in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def get_current_language() -> str:
    if has_request_context():
        cached = getattr(g, "current_language", None)
        if cached:
            return normalize_language(cached)
        return normalize_language(request.cookies.get(LANGUAGE_COOKIE_NAME))
    return DEFAULT_LANGUAGE


def _lookup_translation(key: str, language: str) -> str | None:
    cursor: object = TRANSLATIONS.get(language, {})
    for segment in key.split("."):
        if not isinstance(cursor, dict) or segment not in cursor:
            return None
        cursor = cursor[segment]
    return cursor if isinstance(cursor, str) else None


def raw_translation(key: str, *, language: str | None = None) -> str:
    resolved_language = normalize_language(language or get_current_language())
    value = _lookup_translation(key, resolved_language)
    if value is None and resolved_language != DEFAULT_LANGUAGE:
        value = _lookup_translation(key, DEFAULT_LANGUAGE)
    return value or key


def translate(key: str, **kwargs) -> str:
    value = raw_translation(key)
    if kwargs:
        try:
            return value.format(**kwargs)
        except (KeyError, ValueError):
            return value
    return value


def build_client_translations() -> dict[str, str]:
    return {key: raw_translation(key) for key in CLIENT_TRANSLATION_KEYS}


def get_book_status_choices() -> list[tuple[str, str]]:
    return [(value, translate(f"book_status.{value}")) for value in BOOK_STATUS_ORDER]


def get_annotation_tag_choices() -> list[tuple[str, str]]:
    return [(value, translate(f"annotation_tag.{value}")) for value in ANNOTATION_TAG_ORDER]


def book_status_label(value: str | None) -> str:
    if not value:
        return ""
    return translate(f"book_status.{value}")


def annotation_tag_label(value: str | None) -> str:
    if not value:
        return ""
    return translate(f"annotation_tag.{value}")
